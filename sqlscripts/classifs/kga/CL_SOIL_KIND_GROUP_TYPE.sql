CREATE TABLE CL_SOIL_KIND_GROUP_TYPE
(
    ID             int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    GT_ID          int NOT NULL REFERENCES CL_SOIL_GROUP_TYPE (id) ON DELETE SET NULL,
    SK_ID          int NOT NULL REFERENCES CL_SOIL_KIND (id) ON DELETE SET NULL,
    STATUS         varchar,
    CREATOR        VARCHAR,
    DATE_CR        varchar,
    ARCHIVER       VARCHAR,
    DATE_ARCH      varchar
);
