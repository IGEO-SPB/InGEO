COMMENT ON COLUMN construction_mesh.pot_mass_with_sediment IS 'Вес тигля с осадком, gam1 [18.06.2023]';
COMMENT ON COLUMN construction_mesh.mass_X_more_70 IS 'Размер частиц, вес частиц, Y70 [18.06.2023]';
COMMENT ON COLUMN construction_mesh.HCl IS 'Реакция с HCl [18.06.2023]';


