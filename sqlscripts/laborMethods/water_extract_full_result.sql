CREATE TABLE water_extract_full_result
(
    id            int GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sample_id     int NOT NULL REFERENCES sample (id) ON DELETE SET NULL,

    is_archive    boolean,
    is_blocked    boolean,

    depth_from    float4,
    depth_to      float4,
    sampling_date date,

    HCO3_v        float4,
    HCO3_eq       float4,
    HCO3_proc     float4,

    CO3_v         float4,
    CO3_eq        float4,
    CO3_proc      float4,

    Cl_v          float4,
    Cl_eq         float4,
    Cl_proc       float4,

    SO4_v         float4,
    SO4_eq        float4,
    SO4_proc      float4,

    an_sum_eq     float4,
    an_sum_proc   float4,

    Ca_v          float4,
    Ca_eq         float4,
    Ca_proc       float4,

    Mg_v          float4,
    Mg_eq         float4,
    Mg_proc       float4,

    Na_v          float4,
    Na_eq         float4,
    Na_proc       float4,

    kat_sum_eq    float4,
    kat_sum_proc  float4,

    ion_sum       float4,

    Dry           float4,
    O2            float4,
    pH            float4,
    gum           float4
);